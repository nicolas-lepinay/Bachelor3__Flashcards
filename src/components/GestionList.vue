<template>
    <div class="list-container">
        <div 
            v-for="item in items" 
            v-bind:key="item.id" 
            @click="go(item.id)">
            <img v-if="item.icon" :src="require(`../assets/icons/${item.icon}`)"/>
            <p>{{ item.name ? item.name : item.question }}</p>
            <div class="actions">
                <button class="action-btn" @click="showModal = true">üñâ</button>
                <button class="action-btn del" @click="showModal = true">üóëÔ∏è</button>
            </div>
        </div>
    </div>
    
    <!--  -->
</template>

<script>

export default {
    name: 'GestionList',
    props: ["baseItems"],
    methods: {
        go: function(itemId) {
            this.$emit('choose', itemId)
        }
    },
    computed: {
        items(){
            let str_length = 21;
            let tab = this.baseItems.map((item) => {
                if(!item.icon) str_length = 30;
                if(item.name){
                    if(item.name.length <= str_length) return item;
                    item.name = `${item.name.substring(0,str_length-3)}...`;
                }else{
                    if(item.question.length <= str_length) return item;
                    item.question = `${item.question.substring(0,str_length-3)}...`;
                }
                return item;
            })
            return tab
        }
    }
}
</script>

